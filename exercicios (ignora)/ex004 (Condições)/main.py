idade = int(input("Informe a sua idade: "))

if idade >= 18:
    print("VOCÊ TA LIBERADO")
else:
    print("BLOQUEADO")
#If e Else são estruturas condicionais, no caso desse código: Se(if) o valor da váriavel idade for maio(>) ou igual(=) a 18, vai ser mostrado que você ta liberado, se não(else) vai mostrar que você foi barrado.
