soma = 1+1 #Usamos "+" para somar.
multiplicar = 5*10 #Usamos "*" para multiplicar.
dividir = 9/3 #Usamos "/" para dividir.
Potência = 2**2 #Usamos "**" para fazer potência.

print("soma,", soma)
#Dentro do parentese o que ta dentro das "" vai ser mostrado em escrita primeiro, e logo em seguida fora das "" vai ser mostrado o valor da váriavel.
#Se for imprimir mais de duas coisas no mesmo comando deve ter a ",".
print("Multiplo,", multiplicar)
print("divisão,", dividir)
print("potência,", Potência)