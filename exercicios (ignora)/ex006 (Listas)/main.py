lista_numeros = [5,1,3]
#Os [] após uma váriavel representa que vai ser uma lista
#Criamos uma lista de números e dentro dos [] definimos a sequencia da lista
lista_numeros[0] = 10
#Podemos altera o numero de uma posição colocando dentro dos [] o numero da posição da lista e definindo o novo valor dele logo em seguida

print(lista_numeros [0])
#aqui vai imprimir o valor que ta na posição 0 da lista (A lista é contada atráves do index que começa do 0).
print(lista_numeros [1])
print(lista_numeros [2])

