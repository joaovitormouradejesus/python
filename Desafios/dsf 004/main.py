n = input('Escreva algo: ')


print('O tipo Primitivo desse valor é',type(n))
print('E alfabetico?', n.isalpha())
print('Tem somente espaço?', n.isspace())
print('Ta em maiuscula?', n.isupper())
print('Ta em minuscula?', n.islower())
print('Está capitalizada??', n.istitle())
print('E alfanúmerico?', n.isalnum())
print('E um número?', n.isnumeric())
print('E um decimal?', n.isdecimal())
